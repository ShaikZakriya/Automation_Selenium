package imran;

public class ArrayIfelse_statment {

	public static void main(String[] args) {
int num[]= {10,20,22,33,44,66,75};
	
     for(int i=0;i<num.length;i++)
     {
    	 if (num[i] % 2 ==0)
    	 {
    		 System.out.println(num);
    		
    	
    	 }
    	 
    	 else
    	 {
    		 System.out.println(num +" is not a multiple of 2");
    	 }
     }


	}

}
